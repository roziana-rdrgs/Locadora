    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        // TODO add your handling code here:
        //editar os dados do cliente
        Clientes cli = new Clientes();
        int linha = this.tblClientes.getSelectedRow();

        
        if (JOptionPane.showConfirmDialog(this, "O que você deseja fazer??") == 0){
            //selecionou a opção "sim"
            cli.setNome(String.valueOf(model.getValueAt(linha, 1)));
            cli.setCpf(String.valueOf(model.getValueAt(linha, 2)));
            cli.setTelefone(String.valueOf(model.getValueAt(linha, 3)));
            //...
            
            posicionaRegistro(cli);
            jTabbedPane1.setSelectedIndex(0);
        }
    }//GEN-LAST:event_tblClientesMouseClicked
    //Verifica se os campos que são obrigatórios foram preenchidos
    public boolean validaCampos(String nome, String cpf){
        if(nome.isEmpty() || cpf.isEmpty()){
            JOptionPane.showMessageDialog(this, "Nome e CPF são informações obrigatórias.");
            return false;
        }
        return true;
    }
    
    //Posiciona os dados do registro nos campos para edição
    public void posicionaRegistro(Clientes cliente){
        txtNome.setText(cliente.getNome());
        txtCpf.setText(cliente.getCpf());
        //txtDataNasc.setText(cliente.getDataNasc());
        txtTelefone.setText(cliente.getTelefone());
        txtEndereco.setText(cliente.getEndereco());
    }
    //Limpar campos
    public void limparCampos(){
        txtNome.setText("");
        txtCpf.setText("");
        txtDataNasc.setText("");
        txtTelefone.setText("");
        txtEndereco.setText("");    
    }
