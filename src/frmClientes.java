    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        //Salvar
        //testa os campos que são obrigatórios
        if(validaCampos(txtNome.getText(), txtCpf.getText())){
            cliente = new Clientes();
            
            cliente.setNome(txtNome.getText());
            cliente.setCpf(txtCpf.getText());
            cliente.setEndereco(txtEndereco.getText());
            cliente.setTelefone(txtTelefone.getText());
            
           // salvar no banco
           if (locadoraDb.inserirCliente(cliente) ==  1) {
               JOptionPane.showMessageDialog(this, cliente.getNome() + " cadastrado!");
           }
           //limpa os campos
           limparCampos();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        //Cancelar - limpa os campos e fecha o form
        limparCampos();
        dispose();
    }//GEN-LAST:event_btnCancelarActionPerformed

