    public boolean validaCampos(String filme, String cliente){
        if(filme.isEmpty() || cliente.isEmpty()){
            JOptionPane.showMessageDialog(this, "Cliente e Filme são informações obrigatórias!");
            return false;
        }
        return true;
    }  
    
