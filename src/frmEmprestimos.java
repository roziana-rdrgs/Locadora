    private void tblEmprestimosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmprestimosMouseClicked
        // TODO add your handling code here:
        //editar os dados da venda
        Emprestimos emp = new Emprestimos();
        int linha = this.tblEmprestimos.getSelectedRow();

        if (JOptionPane.showConfirmDialog(this, "Você deseja editar os dados?", "Empréstimo", 
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == 0){
            //selecionou a opção "sim"
            emp.setCliente(String.valueOf(model.getValueAt(linha, 1)));
            emp.setFilme(String.valueOf(model.getValueAt(linha, 2)));
           
            //...
            
            posicionaRegistro(emp);
            jTabbedPane1.setSelectedIndex(0);
        }
    }//GEN-LAST:event_tblEmprestimosMouseClicked

